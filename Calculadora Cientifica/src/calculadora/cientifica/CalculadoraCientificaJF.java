    //Método trigonometria
    public void Trigonometria() {
        //Condição que valida se o radio "sexagesimal" está selecionado
        if (radio_sexages.isSelected()) {
            //Switch da variável "funcoes"
            switch(funcoes){
                //Seno
                case "sen":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" sen( " +valor1+" )");
                    sin = Math.sin(Math.PI*(valor1)/180);
                    entrada_principal.setText(sin+" ");
                    break;
                //Cosseno
                case "cos":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" cos( " +valor1+" )");
                    cos = Math.cos(Math.PI*(valor1)/180);
                    entrada_principal.setText(cos+" ");
                    break;
                //Tangente
                case "tan":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" tan( " +valor1+" )");
                    tan = Math.tan(Math.PI*(valor1)/180);
                    entrada_principal.setText(tan+" ");
                    break;
            }
        }else{
            //Condição que valida se o radio "radianos" está selecionado
        if (radio_rad.isSelected()) {
            //Switch da variável "funcoes"
            switch(funcoes){
                //Seno
                case "sen":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" senr( " +valor1+" )");
                    sin = Math.sin(valor1);
                    entrada_principal.setText(sin+" ");
                    break;
                //Cosseno
                case "cos":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" cosr( " +valor1+" )");
                    cos = Math.cos(valor1);
                    entrada_principal.setText(cos+" ");
                    break;
                //Tangente
                case "tan":
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    entrada_secundaria.setText(" tanr( " +valor1+" )");
                    tan = Math.tan(valor1);
                    entrada_principal.setText(tan+" ");
                    break;
            }
        }
        }
    }
