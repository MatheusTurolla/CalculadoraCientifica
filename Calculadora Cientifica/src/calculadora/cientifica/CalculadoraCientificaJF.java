    //Método com as operações de soma, subtração, multiplicação e divisão
    private void Operacoes(double valor1, double valor2) {
        //Switch da variável "tipoOperacoes"
        switch(tipoOperacoes){
            //Case para a função de soma
            case "+":
                resultado = valor1 + valor2;
                entrada_principal.setText(resultado + "");
                valor1 = Double.parseDouble(entrada_principal.getText());
                break;
            //Case para a função de subtração
            case "-":
                resultado = valor1 - valor2;
                entrada_principal.setText(resultado + "");
                valor1 = Double.parseDouble(entrada_principal.getText());
                break;
            //Case para a função de multiplicação
            case "*":
                resultado = valor1 * valor2;
                entrada_principal.setText(resultado + "");
                valor1 = Double.parseDouble(entrada_principal.getText());
                break;
            //Case para a função de divisão
            case "/":
                //Controle de erros
                //Se a operação for um número qualquer dividido por 0, aparece erro na tela
                if (valor2 == 0) {
                    entrada_principal.setText("Erro ");
                    break;
                }else{
                   resultado = valor1 / valor2;
                    entrada_principal.setText(resultado + "");
                    valor1 = Double.parseDouble(entrada_principal.getText());
                    break; 
                }
                
        }
    }
    
